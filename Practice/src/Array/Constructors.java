package Array;

public class Constructors {

	public Constructors() {
	      this(20);
		}
	    public Constructors(int i) {
	    	this("mohan");
	    	System.out.println(i);
	    }
	    
	    public Constructors(String a) {
	    	this(10.10f);
	    	System.out.println(a);
	    }
	    public Constructors(float b) {
	    	System.out.println(b);
	    	
	    
}}
